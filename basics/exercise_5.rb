# Write a program that outputs the factorial of the numbers 5, 6, 7, and 8.
# factorial: the product of an integer and all the integers below it;
# e.g., factorial four ( 4! ) is equal to 24.

five = 5 * 4 * 3 * 2 * 1
six = 6 * 5 * 4 * 3 * 2 * 1
seven = 7 * 6 * 5 * 4 * 3 * 2 * 1
eight = 8 * 7 * 6 * 5 * 4 * 3 * 2 * 1

puts five
puts six
puts seven
puts eight
